//name və surname xanasındakı dəyərləri alıb loggedİnAdmin-ə mənimsədir
    private String[] getInput() {
        if (txtName.getText() != null && !txtName.getText().isEmpty()) {
//            Config.loggedInAdmin.setName(txtName.getText());
            name=txtName.getText();
        }
        if (txtSurname.getText() != null && !txtSurname.getText().isEmpty()) {
//            Config.loggedInAdmin.setSurname(txtSurname.getText());
            surname=txtSurname.getText();
        }
        if (txtPassword.getText() != null && !txtPassword.getText().isEmpty()) {
//            Config.loggedInAdmin.setPassword(txtPassword.getText());
            password=txtPassword.getText();
        }
        return new String[]{name,surname,password};
    }

    //msg və red ya da green alır və alert pəncərəsində alert verir.
    private void alert(String msg, String color) {
        if (color.equals("green")) {
            lblAlert.setForeground(Color.green);
        } else if (color.equals("red")) {
            lblAlert.setForeground(Color.red);
        }else if (color.equals("yellow")) {
            lblAlert.setForeground(Color.yellow);
        }
        lblAlert.setText(msg);

    }

    //bazadan məlumatlı alıb, id,name,surname,password xanasını doldurur
    public void fillTable() {
        Vector<Vector> rows = new Vector<>();
        List<User> users = userDao.getAll();

        for (User user : users) {
            Vector<String> row = new Vector<>();
            row.add(String.valueOf(user.getId()));
            row.add(user.getName());
            row.add(user.getSurname());
            row.add(user.getPassword());
            rows.add(row);
        }
        DefaultTableModel dtm = new DefaultTableModel(rows, new Vector(Arrays.asList("ID", "Name", "Surname", "Password")));
        tblUser.setModel(dtm);
    }
