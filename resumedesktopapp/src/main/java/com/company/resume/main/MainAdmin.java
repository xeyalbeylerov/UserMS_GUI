    private Integer getSelectedRow() {
        String strRow = (String) tblUser.getValueAt(tblUser.getSelectedRow(), 0);
        System.out.println(strRow);
        Integer sRow = Integer.valueOf(strRow);
        System.out.println(sRow);
        selectedRowId=sRow;
        return sRow;
    }

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed

        Integer sRow = getSelectedRow();
        if (sRow != null) {
            userDao.removeUser(sRow);
            fillTable();
            System.out.println(sRow);
        } else {
            System.err.println("Bir row secilmeyib");
            alert("User not selected", "red");
        }


    }//GEN-LAST:event_btnDeleteActionPerformed

